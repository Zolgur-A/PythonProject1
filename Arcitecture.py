"""Prompt for generating understandable C3/C4 architecture diagrams."""

SYSTEM_NAME = "Email Summary Agent"
DIAGRAM_LANGUAGE = "Russian"
OUTPUT_FORMAT = "Mermaid или PlantUML"

ARCHITECTURE_DIAGRAM_PROMPT = f"""
Ты — системный архитектор и технический аналитик. Сформируй понятное описание
проекта и архитектурные диаграммы для системы "{SYSTEM_NAME}" на уровнях C3 и C4
по модели C4.

Язык описания: {DIAGRAM_LANGUAGE}.
Формат диаграмм: {OUTPUT_FORMAT}.

0. Объясни проект простыми словами
Перед диаграммами обязательно дай короткое человеческое описание проекта:
- что это за система;
- какую проблему она решает;
- кто ей пользуется;
- какие данные она принимает;
- какой результат отдаёт;
- из каких крупных частей состоит;
- что происходит с письмом от момента получения до готовой справки.

Описание должно быть понятно человеку, который впервые видит проект и не знает
термины C3, C4, ingestion, OCR, LLM, queue и webhook.

Короткая суть проекта:
"{SYSTEM_NAME}" — это агент, который помогает пользователю быстро разбираться
в почте. Он получает письмо и вложения, извлекает из них полезный текст,
приводит данные к единому виду, отправляет их в LLM-модель, получает краткую
справку и показывает пользователю главное: смысл письма, важные факты, задачи,
сроки, риски и необходимость ручной проверки.

Бизнес-ценность:
- пользователь тратит меньше времени на чтение длинных писем;
- важные задачи и сроки не теряются;
- вложения учитываются вместе с текстом письма;
- система может работать вручную, по команде или автоматически при новом письме.

Основные пользователи:
- обычный пользователь, который читает готовую краткую справку;
- администратор, который подключает почтовый ящик и настраивает доступы;
- разработчик/оператор, который следит за ошибками, очередями и качеством обработки.

Входные данные:
- текст письма;
- HTML-тело письма;
- метаданные: отправитель, получатели, тема, дата, thread id, message id;
- вложения: PDF, DOCX, TXT, изображения, сканы;
- события от почтового провайдера о новых письмах.

Выходные данные:
- краткое резюме письма на 1–2 предложения;
- важные факты;
- список задач;
- сроки;
- риски;
- краткое описание вложений;
- оценка уверенности результата;
- флаг needs_human_review, если нужна ручная проверка.

Словарь терминов:
- LLM: языковая модель, которая делает краткую справку и выделяет смысл.
- OCR: распознавание текста на изображениях и сканах.
- Webhook: уведомление от внешней системы о событии, например о новом письме.
- Queue/Event Bus: очередь задач, которая позволяет обрабатывать письма в фоне.
- Worker: фоновый обработчик, который выполняет тяжёлую работу.
- Ingestion: приём исходных данных в систему.
- Idempotency: защита от повторной обработки одного и того же письма.
- Metadata DB: база данных со статусами, результатами и служебной информацией.
- Object Storage: хранилище исходных писем, вложений и извлечённого текста.

1. Цель системы
Система получает письма и вложения, извлекает из них текст, нормализует данные,
формирует краткую справку через LLM, выделяет задачи, сроки, риски и доставляет
результат пользователю через UI, чат или уведомления.

2. Режимы работы системы
Опиши все режимы отдельно и покажи, какие компоненты участвуют в каждом режиме.

Режим 1 — ручная загрузка:
1. Пользователь вставляет текст письма или загружает файл.
2. Backend принимает входные данные.
3. Worker извлекает текст.
4. LLM формирует справку.
5. Пользователь видит результат в UI или чате.

Режим 2 — обработка по команде:
1. Пользователь запускает команду, например: "обработай последние 20 писем".
2. Scheduler или Job Orchestrator создаёт batch-задачу.
3. Mail Connector получает письма из Gmail или Microsoft 365.
4. Очередь распределяет задачи между worker-ами.
5. Система сохраняет и отправляет результаты.

Режим 3 — автоматическая обработка новых писем:
1. Почтовый провайдер присылает webhook/push notification.
2. Backend быстро подтверждает получение события.
3. Событие попадает в очередь.
4. Worker получает письмо и вложения.
5. Система формирует справку и уведомляет пользователя.

3. Основной сценарий работоспособности
1. Пользователь вручную загружает письмо/файл или запускает обработку писем.
2. Почтовый провайдер может прислать webhook/push notification о новом письме.
3. Backend принимает входящие данные и быстро подтверждает приём события.
4. Ingestion Service сохраняет raw payload и публикует задачу в очередь.
5. Idempotency Guard проверяет, не обрабатывалось ли письмо раньше.
6. Mail Connector получает письмо, тело письма, метаданные и вложения.
7. Extraction Worker извлекает текст из HTML, plain text, PDF, DOCX, TXT,
   изображений и сканов через OCR fallback.
8. Message Normalizer приводит письмо и вложения к единой JSON-структуре.
9. Summarization Service собирает промпт, вызывает LLM и получает structured output.
10. Structured Output Validator проверяет JSON-ответ модели.
11. Metadata DB хранит статусы, summaries, idempotency keys и audit trail.
12. Object Storage хранит raw emails, вложения и извлечённый текст.
13. Notification Service отправляет готовую справку пользователю.

4. Обязательные свойства работоспособности
- Idempotency: одно письмо не должно обрабатываться повторно.
- Async processing: webhook не выполняет тяжёлую обработку синхронно.
- Retry policy: ошибки extraction, OCR, LLM и доставки должны повторяться безопасно.
- Observability: логируются статусы пайплайна, ошибки, latency и queue depth.
- Security: raw emails, вложения и summaries хранятся с контролем доступа.
- Human review: при низкой уверенности результата система помечает справку для проверки.
- Audit trail: важные действия записываются для последующего анализа.

5. C3 уровень — Component Diagram
Сначала кратко объясни, что C3 показывает внутренние компоненты контейнеров:
Backend, Worker, Summarization и Delivery.

Покажи компоненты внутри Backend, Worker, Summarization и Delivery слоёв.

Backend components:
- Webhook Controller: принимает события от Gmail/Microsoft 365.
- Manual Upload Controller: принимает ручную загрузку текста и файлов.
- Job Orchestrator: создаёт batch/realtime/manual задачи обработки.
- Idempotency Guard: проверяет message_id/thread_id/provider_id.
- Raw Message Persister: сохраняет исходные данные.
- Task Publisher: публикует задачи в Queue/Event Bus.

Worker components:
- Mail Fetcher: получает письмо и вложения из Mail Provider.
- Message Normalizer: формирует единый JSON документа.
- Body Extractor: извлекает текст из HTML/plain body.
- Attachment Classifier: определяет тип вложения.
- Document Text Extractor: извлекает текст из PDF/DOCX/TXT.
- OCR Adapter: распознаёт изображения и сканы.
- Chunker: делит длинные тексты на фрагменты.
- Extraction Result Writer: сохраняет результат извлечения.

Summarization components:
- Prompt Builder: собирает инструкции и контекст письма.
- Summary Generator: вызывает LLM Platform.
- Structured Output Validator: проверяет JSON schema.
- Action/Risk/Deadline Extractor: выделяет задачи, риски и сроки.
- Confidence Scorer: оценивает уверенность результата.
- Summary Writer: сохраняет итоговую справку.

Delivery components:
- Digest Composer: готовит короткий текст для пользователя.
- Notification Dispatcher: отправляет результат в чат, UI или email.
- Audit Logger: фиксирует события обработки и доставки.

6. C4 уровень — Code/Class Diagram
Сначала кратко объясни, что C4 показывает кодовый уровень: ключевые классы,
модули, методы, поля и связи между ними.

Покажи ключевые классы/модули и их ответственность.

Предложи следующие классы или модули:
- EmailProcessingJob: описание задачи обработки письма.
- EmailMessage: нормализованное письмо с метаданными.
- Attachment: описание вложения и его типа.
- ExtractedDocument: результат извлечения текста.
- SummaryResult: итоговая справка с actions, deadlines, risks, confidence.
- IdempotencyService: проверка повторной обработки.
- MailConnector: получение писем и вложений.
- ExtractionPipeline: управление извлечением текста.
- SummarizationPipeline: сбор промпта, вызов LLM, валидация ответа.
- NotificationClient: доставка результата пользователю.
- AuditLogRepository: запись audit trail.
- EmailRepository: работа с метаданными и статусами.
- ObjectStorageClient: работа с raw files и extracted text.

Для каждого класса укажи:
- назначение;
- основные поля;
- основные методы;
- связи с другими классами;
- какие ошибки он обрабатывает.

7. Потоки данных
На диаграммах обязательно покажи поток:
User -> UI/API -> Ingestion -> Queue -> Worker -> LLM -> DB/Storage -> Notification -> User.

Дополнительно покажи поток для вложений:
Attachment -> Attachment Classifier -> Document Text Extractor/OCR -> ExtractedDocument -> Summary.

8. Failure flow
Добавь отдельный блок с обработкой ошибок:
- duplicate email: Idempotency Guard останавливает повторную обработку;
- недоступен mail provider: задача уходит в retry;
- повреждённое вложение: вложение помечается как failed, письмо всё равно может быть обработано;
- ошибка OCR: используется fallback или ставится needs_human_review;
- недоступна LLM: задача повторяется позже;
- невалидный JSON от LLM: выполняется повторный запрос или validation repair;
- ошибка доставки уведомления: результат остаётся в БД, доставка повторяется.

9. Требования к результату
Результат должен быть пригоден для передачи архитектору, разработчикам и
нетехническому заказчику. Сначала дай понятное описание проекта, затем C3,
затем C4, затем список рисков и вопросов для уточнения.
"""


def get_architecture_diagram_prompt() -> str:
    """Return the prompt for generating understandable C3 and C4 diagrams."""
    return ARCHITECTURE_DIAGRAM_PROMPT.strip()


def main() -> None:
    """Print the architecture diagram prompt."""
    print(get_architecture_diagram_prompt())


if __name__ == "__main__":
    main()
